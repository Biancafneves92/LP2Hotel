    public boolean TrataCampos() {
    boolean retorno = false;
    try {
        acompNm = tf_acompNm.getText().trim();
        acompSnm = tf_acompSnm.getText().trim();
        acompEmail = tf_acompEmail.getText().trim();
        
        acompCPF = Long.parseLong(tf_acompCPF.getText().trim());
        acompRG = Integer.parseInt(tf_acompRG.getText().trim());
        
        acompMesNasc = cb_acompMesNasc.getItemAt(cb_acompMesNasc.getSelectedIndex());
        acompDianasci = cb_AcompDtnasci.getSelectedIndex();
        acompAnonasc = cb_acompAnonasc.getSelectedIndex();
        
       if(br_acompFemi.isSelected()) acompSexo=("F"); else acompSexo=("M");
       retorno = true;
        }
    catch (NumberFormatException erro) {
        JOptionPane.showMessageDialog(null, "CPF e RG devem ser campos num√©ricos","Erro",JOptionPane.ERROR_MESSAGE);
    }
    return retorno;
}
    
    
    public void ConsisteCamposPreenchidos() {
    if (tf_acompNm.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha o Nome!","Erro",JOptionPane.ERROR_MESSAGE);
    }
    if (tf_acompSnm.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha o Sobrenome!","Erro",JOptionPane.ERROR_MESSAGE);
    }
    if(tf_acompCPF.getText().trim().isEmpty()) {
     JOptionPane.showMessageDialog(null, "Preencha o CPF!","Erro",JOptionPane.ERROR_MESSAGE);   
    }
    if(tf_acompEmail.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(null, "Preencha o Email!","Erro",JOptionPane.ERROR_MESSAGE);  
    }
    if((!br_acompFemi.isSelected())&&(!br_acompMasc.isSelected())){
        JOptionPane.showMessageDialog(null, "Selecione o Sexo!","Erro",JOptionPane.ERROR_MESSAGE);
    }
    if(cb_acompAnonasc.getSelectedIndex()==-1) {
        JOptionPane.showMessageDialog(null, "Selecione o ano de nascimento","Erro",JOptionPane.ERROR_MESSAGE);
    }
    if(cb_AcompDtnasci.getSelectedIndex()==-1) {
        JOptionPane.showMessageDialog(null, "Selecione a data de nascimento","Erro",JOptionPane.ERROR_MESSAGE);
    }
    if(cb_acompMesNasc.getSelectedIndex()==-1) {
        JOptionPane.showMessageDialog(null, "Selecione o m√™s de nascimento","Erro",JOptionPane.ERROR_MESSAGE);
    }

}

